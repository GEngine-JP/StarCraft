package util;

public class Resource {

	private int mine;

	private int man;

	public Resource(int mine, int man) {
		super();
		this.mine = mine;
		this.man = man;
	}

	public int getMan() {
		return man;
	}

	public void setMan(int man) {
		this.man = man;
	}

	public int getMine() {
		return mine;
	}

	public void setMine(int mine) {
		this.mine = mine;
	}

}
